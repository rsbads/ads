def sum_two_number(
    a:float,
    b:float = 5
) -> float: 
    """ Sum two values.

    Take two variable and sum.

    Args:
        a (float): First number.
        b (float, optional): Second number. Defaults to 5.

    Returns:
        float: The sum of 'a' and 'b'.
    """

    number = a + b
    
    return number